public class Main {
    public static void main(String[] args) {
        // Задача №1
        System.out.println("Задача №1:");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        System.out.println();

        // Задача №2
        System.out.println("Задача №2");
        for (int i = 10; i > 0; i--) {
            System.out.println(i);
        }
        System.out.println();

        // Задача №3
        System.out.println("Задача №3:");
        for (int i = 0; i <= 17; i = i + 2) {
            System.out.println(i);
        }
        System.out.println();

        // Задача №4
        System.out.println("Задача №4:");
        for (int i = 10; i >= -10; i--) {
            System.out.print(i + " ");
        }
        System.out.println();
    }
}